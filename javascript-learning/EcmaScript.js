// ECMAScript (ES) is a scripting-language specification standardized by Ecma International. It is used to create scripting languages like JavaScript. 
// Here is a brief overview of each ECMAScript version along with their release years and key features:

// ### ES1 (1997)
// - **Year Released:** 1997
// - **Key Features:**
//   - Initial version
//   - Basic language constructs, types, and objects

// ### ES2 (1998)
// - **Year Released:** 1998
// - **Key Features:**
//   - Editorial changes to align with the ISO/IEC 16262 international standard

// ### ES3 (1999)
// - **Year Released:** 1999
// - **Key Features:**
//   - Regular expressions
//   - Better string handling
//   - New control statements (try/catch)

// ### ES4 (Abandoned)
// - **Note:** Development of ES4 was abandoned due to significant differences among stakeholders.

// ### ES5 (2009)
// - **Year Released:** 2009
// - **Key Features:**
//   - "Strict mode" for improved error checking
//   - JSON support
//   - New Array methods (forEach, map, filter, etc.)
//   - Property getters and setters
//   - Improved object properties (Object.defineProperty, Object.keys)

// ### ES5.1 (2011)
// - **Year Released:** 2011
// - **Key Features:**
//   - Minor updates to align with the ECMAScript specification and international standards (ISO/IEC 16262:2011)

// ### ES6 / ECMAScript 2015 (2015)
// - **Year Released:** 2015
// - **Key Features:**
//   - let and const for block-scoped variables
//   - Arrow functions
//   - Classes
//   - Template literals
//   - Default parameters
//   - Destructuring assignments
//   - Promises
//   - Modules (import/export)
//   - Symbols
//   - Iterators and for...of loops
//   - Maps, Sets, WeakMaps, WeakSets
//   - The spread operator (...)

// ### ES7 / ECMAScript 2016 (2016)
// - **Year Released:** 2016
// - **Key Features:**
//   - Array.prototype.includes
//   - Exponentiation operator (**)

// ### ES8 / ECMAScript 2017 (2017)
// - **Year Released:** 2017
// - **Key Features:**
//   - Async functions (async/await)
//   - Object.values/Object.entries
//   - String padding (padStart, padEnd)
//   - Object.getOwnPropertyDescriptors
//   - Trailing commas in function parameter lists

// ### ES9 / ECMAScript 2018 (2018)
// - **Year Released:** 2018
// - **Key Features:**
//   - Asynchronous iteration (for-await-of)
//   - Promise.prototype.finally
//   - Rest/Spread properties for objects
//   - Regular Expression improvements (lookbehind assertions, named capture groups)

// ### ES10 / ECMAScript 2019 (2019)
// - **Year Released:** 2019
// - **Key Features:**
//   - Array.prototype.flat and Array.prototype.flatMap
//   - Object.fromEntries
//   - String.prototype.trimStart and String.prototype.trimEnd
//   - Optional catch binding
//   - Function.prototype.toString revision

// ### ES11 / ECMAScript 2020 (2020)
// - **Year Released:** 2020
// - **Key Features:**
//   - BigInt
//   - Dynamic import()
//   - Nullish coalescing operator (??)
//   - Optional chaining operator (?.)
//   - Promise.allSettled
//   - globalThis
//   - String.prototype.matchAll

// ### ES12 / ECMAScript 2021 (2021)
// - **Year Released:** 2021
// - **Key Features:**
//   - Logical assignment operators (&&=, ||=, ??=)
//   - Numeric separators (1_000_000)
//   - String.prototype.replaceAll
//   - WeakRefs and FinalizationRegistry
//   - Promise.any
//   - Private class methods and accessors

// ### ES13 / ECMAScript 2022 (2022)
// - **Year Released:** 2022
// - **Key Features:**
//   - Top-level await
//   - Class fields and private methods
//   - RegExp match indices (d flag)
//   - Static blocks in classes

// ### ES14 / ECMAScript 2023 (2023)
// - **Year Released:** 2023
// - **Key Features:**
//   - Array and TypedArray findLast and findLastIndex
//   - Hashbang (#!) in scripts
//   - Symbols as WeakMap keys
//   - Change Array by copy methods (toReversed, toSorted, toSpliced)

// // Each new version of ECMAScript builds upon the previous ones, introducing new features and improvements to make the language more powerful, efficient, and easier to use.